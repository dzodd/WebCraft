<H1>News Feed là gì? </h1> News feed bao gồm các status, hình ảnh, video, liên kết, hoạt động ứng dụng và like. Đôi khi bạn sẽ thấy xuất hiện cả quảng cáo của các trang fanpage trên News Feed của mình.

Làm thế nào News Feed của tôi xác định được đâu là nội dung là thú vị nhất ?
Thuật toán của News Feed sử dụng một số yếu tố để xác định các bài đăng top, bao gồm cả số lượng comment (bình luận), ai đăng bài post đấy, và loại bài đó là gì (ví dụ: hình ảnh, video, cập nhật trạng thái, vv.)

Nếu bạn cảm thấy News Feed của bạn đang bỏ lỡ những câu chuyện bạn muốn nhìn thấy hoặc xuất hiện những cái mà bạn không muốn xem, sử dụng các tùy chỉnh News Feed để điều chỉnh lại cài đặt của bạn.

Tại sao tôi thấy trong News Feed của tôi những câu chuyện về những người không phải là bạn bè tôi hay nhóm mà tôi không phải là thành viên?
Bạn có thể thấy các bài viết trong News Feed của bạn từ những người lạ, lý do nằm ở chỗ bạn bè của bạn  thích hoặc tương tác với bài đó. Hãy nhớ rằng chúng tôi chỉ cho bạn thấy bài viết của ai đó nếu họ đang thiết lập để công cộng hoặc bạn bè của bạn bè.

Những câu chuyện từ các nhóm mở có thể xuất hiện trong News Feed của bạn từ các nhóm mở được công khai.

<br>Lý do tại sao tôi có thể nhìn thấy News Feed câu chuyện về một người vẫn chưa chấp nhận yêu cầu kết bạn của tôi?
Khi bạn gửi một yêu cầu kết bạn tới ai đó, những câu chuyện mà họ đã chia sẻ (ở dạng Public) có thể xuất hiện trong News Feed của bạn. Bằng cách này bạn không cần phải chờ đợi họ chấp nhận yêu cầu kết bạn của bạn thì mới xem được thông tin cập nhật nào đó của họ. Xin lưu ý rằng News Feed sẽ không hiển thị những câu chuyện mà chỉ những người là bạn bè thực sự mới được phép nhìn thấy. Người chưa chấp nhận yêu cầu kết bạn của bạn cũng sẽ xuất hiện trên trang bạn bè của bạn. Tại đây, bạn có thể thêm họ vào danh sách bạn bè.

Nếu bạn không muốn xem những câu chuyện từ một người vẫn chưa chấp nhận yêu cầu kết bạn của bạn, bạn có thể ẩn chúng từ News Feed của bạn bằng cách di chuột trên phía trên bên phải của một câu chuyện và nhấn vào liên kết Hide (Ẩn). Bạn cũng có thể loại bỏ chúng như một người bạn bằng cách nhấn vào X bên cạnh tên của họ trên trang bạn bè của bạn .

<br>    Lý do tại sao một người nào đó tôi chưa chấp nhận như một người bạn có thể nhìn thấy trên News Feed của họ những câu chuyện về tôi ?
Khi ai đó gửi cho bạn một yêu cầu kết bạn, câu chuyện nào đó về bạn (mà được để ở chế độ public) có thể xuất hiện trong News Feed của họ. Nếu bạn từ chối yêu cầu kết bạn từ người này, những câu chuyện về bạn sẽ không xuất hiện trong News Feed của họ nữa.

